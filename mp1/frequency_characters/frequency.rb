count = Array.new(26)
c = 0
puts "Enter the string"
string = gets.chomp
 while  string[c] != '\0' 
   
      if ( string[c] >= 'a' && string[c] <= 'z' ) 
         count[string[c]]+=1
 	end
      c+=1
end
 for c in 0..26
    if( count[c] != 0 )
         puts "#{c+'a'} occurs #{count[c]} times in the entered string."  
    end
end
 
